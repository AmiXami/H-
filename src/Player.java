    public class  Player {
    private int Hp;
    private int Damage;
    private String Attributes;






        public int getHp() {
            return Hp;
        }

        public void setHp(int hp) {
            Hp = hp;
        }

        public int getDamage() {
            return Damage;
        }

        public void setDamage(int damage) {
            Damage = damage;
        }

        public String getAttributes() {
            return Attributes;
        }

        public void setAttributes(String attributes) {
            Attributes = attributes;
        }
    }
